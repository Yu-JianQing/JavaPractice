package aboutUtil;

public class aboutString {
	public static void main(String[] args) {
		String string="no pains no gains as long long ago";
		
		boolean bl1=string.startsWith("nop");//不限定个数
		boolean bl2=string.endsWith("ago");
		
		int res=string.compareTo("tom");
		
		boolean bl3=string.contains("long");
		
		String str=string.substring(5);
		
		System.out.println(bl1+"  "+bl2+"  "+res+"  "+bl3+"  "+str);
	}
}
